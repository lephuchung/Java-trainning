
public class ViDu {
	public static void main(String[] args) {
		
		// Khai bao bien
		int a=5;
		boolean b=false;
		
		// Thuc hanh phep toan 1 toan hang
		System.out.println("+a = "+ (+a));
		System.out.println("-a = "+ (-a));
		System.out.println("!b = "+ (!b));
		
		System.out.println("++a = "+ (++a));
		System.out.println("a++ = "+ (a++));
		System.out.println("a = "+a);
		
		System.out.println("--a = "+ (--a));
		System.out.println("a-- = "+ (a--));
		System.out.println("a = "+a);
		
	}
}
